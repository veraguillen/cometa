# (finalize-analysis) hasta su migración completa.


def _init_gemini_model_pdf() -> "Any":
    """
    Inicializa el modelo Gemini para extracción de PDFs.
    Mismo orden de credenciales que GeminiAuditor y kpi_mapper._init_gemini_model.
    """
    import json as _json
    import os

    import vertexai
    from google.oauth2 import service_account
    from vertexai.generative_models import GenerativeModel

    project_id = os.getenv("GOOGLE_PROJECT_ID", "cometa-mvp")
    location   = os.getenv("VERTEX_AI_LOCATION", "us-central1")
    model_name = os.getenv("GEMINI_MODEL", "gemini-2.5-flash")

    sa_json_str = os.getenv("GCP_SERVICE_ACCOUNT_JSON")
    sa_path     = os.getenv("GOOGLE_APPLICATION_CREDENTIALS", "cometa_key.json")

    if sa_json_str:
        info = _json.loads(sa_json_str.strip())
        if isinstance(info, str):
            info = _json.loads(info)
        creds = service_account.Credentials.from_service_account_info(info)
        vertexai.init(project=project_id, location=location, credentials=creds)
    elif os.path.isfile(sa_path):
        creds = service_account.Credentials.from_service_account_file(sa_path)
        vertexai.init(project=project_id, location=location, credentials=creds)
    else:
        vertexai.init(project=project_id, location=location)

    return GenerativeModel(model_name)


def _build_pdf_kpi_catalog() -> str:
    """Genera el catálogo compacto de KPIs GIVEN para el prompt de extracción de PDF."""
    import sys

    try:
        with open(_BRAIN_PATH, encoding="utf-8") as f:
            brain = json.load(f)
        lines: list[str] = []
        for m in brain.get("metrics", []):
            if m.get("given_or_silver") != "GIVEN":
                continue
            mid     = m.get("metric_id", "")
            dname   = m.get("display_name", mid)
            unit    = m.get("unit", "?")
            aliases = ", ".join(m.get("aliases", [])[:3])
            entry   = f'  "{mid}": {dname} [unit: {unit}]'
            if aliases:
                entry += f' — aliases: {aliases}'
            lines.append(entry)
        return "\n".join(lines)
    except Exception as exc:
        print(f"[data_contract] WARN: catálogo PDF no disponible: {exc}", file=sys.stderr)
        return ""


def extract_pdf_to_contract(
    pdf_bytes: bytes,
    period_id: str,
) -> "UnifiedKPIContract":
    """
    Extrae métricas de un PDF usando Gemini Vision y las retorna
    como UnifiedKPIContract validado.

    Gemini recibe el PDF completo como dato inline junto al catálogo
    de 109 KPIs y tiene la instrucción de devolver EXCLUSIVAMENTE el
    JSON del contrato.  Si la respuesta no es parseable como
    UnifiedKPIContract, la función levanta ValueError — cero lógica
    condicional post-IA.

    Parameters
    ----------
    pdf_bytes : Contenido binario del PDF.
    period_id : Período canónico inyectado en cada métrica (ej. "P2026Q1M01").

    Returns
    -------
    UnifiedKPIContract validado.

    Raises
    ------
    ValueError  : Si Gemini no devuelve JSON válido conforme al contrato.
    RuntimeError: Si Gemini no está disponible (sin credenciales).
    """
    # Deferred import to avoid circular dependency at module load time
    from src.schemas import UnifiedKPIContract  # noqa: PLC0415

    import json as _json  # noqa: PLC0415

    from vertexai.generative_models import GenerationConfig, Part  # noqa: PLC0415

    catalog = _build_pdf_kpi_catalog()

    from src.core.prompts import build_extraction_prompt  # noqa: PLC0415

    prompt = build_extraction_prompt(
        catalog=catalog,
        period_id=period_id,
        source="PDF",
        raw_data_label="el documento financiero adjunto",
    )

    try:
        model    = _init_gemini_model_pdf()
        config   = GenerationConfig(response_mime_type="application/json", temperature=0.0)
        pdf_part = Part.from_data(data=pdf_bytes, mime_type="application/pdf")
        response = model.generate_content([prompt, pdf_part], generation_config=config)
        parsed   = _json.loads(response.text)
        contract = UnifiedKPIContract.model_validate(parsed)
        return contract

    except Exception as exc:
        raise ValueError(
            f"Gemini no pudo producir un UnifiedKPIContract válido desde el PDF: {exc}"
        ) from exc
