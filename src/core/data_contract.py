def validate_founder_submission(
    kpi_rows: list[dict],
    bucket_id: str,
) -> Optional[dict]:
    """
    Two-gate validator for founder submissions.

    Gate 1 — Mandatory completeness
        Checks that every KPI required for ``bucket_id`` has a valid numeric
        value extracted by Gemini.  Uses SECTOR_REQUIREMENTS as the source of
        truth.  Missing fields are returned with their human-readable label so
        the frontend can render a named rescue form.

    Gate 2 — Sanity ranges
        Checks every extracted value against SANITY_RULES bounds.
        Violations do NOT block storage (the document is already written) but
        are returned so the frontend can demand a written justification that
        will be stored as ``analyst_note`` alongside the corrected value.

    Returns
    -------
    None
        Submission passes both gates — no action required.
    dict
        ``{ "missing_mandatory_fields": [...], "sanity_violations": [...] }``
        At least one list is non-empty when the dict is returned.
    """
    # Index of valid extracted values: kpi_key → numeric_value
    valid_index: dict[str, float] = {
        r["kpi_key"]: r["numeric_value"]
        for r in kpi_rows
        if r.get("is_valid") and r.get("numeric_value") is not None
    }

    # ── Gate 1: mandatory fields ───────────────────────────────────────────
    required_keys = SECTOR_REQUIREMENTS.get(bucket_id, [])
    _label_map = {k["kpi_key"]: k["kpi_label"] for k in KPI_REGISTRY}

    missing_mandatory_fields = [
        {
            "kpi_key": key,
            "label":   _label_map.get(key, key.replace("_", " ").title()),
        }
        for key in required_keys
        if key not in valid_index
    ]

    # ── Gate 2: sanity ranges ──────────────────────────────────────────────
    sanity_violations = []
    for kpi_key, rule in SANITY_RULES.items():
        value = valid_index.get(kpi_key)
        if value is None:
            continue  # absent values are handled by Gate 1, not Gate 2

        lo: Optional[float] = rule["min"]
        hi: Optional[float] = rule["max"]
        violated = (lo is not None and value < lo) or (hi is not None and value > hi)

        if violated:
            sanity_violations.append({
                "kpi_key":               kpi_key,
                "label":                 rule["label"],
                "value":                 value,
                "rule_description":      rule["rule_description"],
                "requires_justification": True,
            })

    if not missing_mandatory_fields and not sanity_violations:
        return None

    return {
        "missing_mandatory_fields": missing_mandatory_fields,
        "sanity_violations":        sanity_violations,
    }


# ── Gemini-powered PDF extractor (UnifiedKPIContract) ─────────────────────────
